    // untuk memasukkan data dari dao ke dalam komponen jtable
    public void tampilkanDataKeTabel() {
        // Mengambil model tabel Anda yang bernama tableStudent
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableStudent.getModel();

        // bersihkan isi tabel lama agar tidak menumpuk ganda
        model.setRowCount(0);

        // panggil dao
        dao.StudentDAO studentDao = new dao.StudentDAO();
        java.util.List<String[]> listMahasiswa = studentDao.getAllStudents();

        // masukkan satu per satu baris data ke tabel
        for (String[] data : listMahasiswa) {
            model.addRow(data);
    }
}

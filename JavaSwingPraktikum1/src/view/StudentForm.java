    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    // ambil baris tabel yang sedang dipilih oleh user
    int barisTerpilih = jTableStudent.getSelectedRow();

    // validasi apakah user sudah memilih baris atau belum
    if (barisTerpilih == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih data pada tabel terlebih dahulu!");
        return;
    }
    
    // konfirmasi dari user sebelum benar-benar menghapus
    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin menghapus data mahasiswa ini?", "Konfirmasi Hapus", 
            javax.swing.JOptionPane.YES_NO_OPTION);

    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        // ambil NIK (id_card) dari kolom ke-0 (kolom pertama) pada baris yang diklik
        String idCard = jTableStudent.getValueAt(barisTerpilih, 0).toString();

        // panggil fungsi delete di studentdao
        dao.StudentDAO studentDao = new dao.StudentDAO();
        boolean sukses = studentDao.deleteStudent(idCard);

        // tampilkan notifikasi status
        if (sukses) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Mahasiswa Berhasil Dihapus!");

            // refresh tabel agar data yang dihapus langsung hilang dari layar
            tampilkanDataKeTabel();

            // kosongkan form input kembali
            txtNama.setText("");
            txtNIM.setText("");
            txtNIK.setText("");
            cmbProdi.setSelectedIndex(0);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Menghapus Data!");
        }
        
        txtNIK.setEditable(true);
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    // ambil teks kata kunci dari kotak pencarian
    String kataKunci = jtxtCari.getText().trim();

    // kotak pencarian kosong, kembalikan tabel ke data default atau semua data
    if (kataKunci.isEmpty()) {
        tampilkanDataKeTabel();
        return;
    }

    // bersihkan isi tabel lama terlebih dahulu
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTableStudent.getModel();
    model.setRowCount(0);

    // panggil fungsi search dari studentdao
    dao.StudentDAO studentDao = new dao.StudentDAO();
    java.util.List<String[]> hasilPencarian = studentDao.searchStudentByName(kataKunci);

    // masukkan hasil pencarian ke dalam komponen tabel
    if (hasilPencarian.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data mahasiswa tidak ditemukan!");
        tampilkanDataKeTabel(); // tampilkan semua data kembali jika tidak ketemu
    } else {
        for (String[] data : hasilPencarian) {
            model.addRow(data);
        }
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jbtnBatalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBatalActionPerformed
        // mengosongkan kembali semua kotak input teks
        txtNama.setText("");
        txtNIM.setText("");
        txtNIK.setText("");

        // mengembalikan pilihan combobox prodi ke pilihan paling atas (-- Pilih Prodi --)
        cmbProdi.setSelectedIndex(0);

        // mengosongkan kotak pencarian jika ada isinya
        jtxtCari.setText("");

        // mengembalikan tampilan tabel ke data default semua data
        tampilkanDataKeTabel();

        // menaruh kursor fokus kembali ke kotak nik agar siap mengetik data baru
        txtNIK.requestFocus();

        // membuka kembali kunci kotak nik untuk data baru
        txtNIK.setEditable(true);
    }//GEN-LAST:event_jbtnBatalActionPerformed

    private void jTableStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableStudentMouseClicked
    try {
        // ambil nomor baris tabel yang diklik
        int baris = jTableStudent.getSelectedRow();
        
        // validasi jika tidak ada baris yang terpilih
        if (baris == -1) {
            return;
        }

        // ambil data dari kolom tabel secara aman
        String nik   = jTableStudent.getValueAt(baris, 0) != null ? jTableStudent.getValueAt(baris, 0).toString() : "";
        String nama  = jTableStudent.getValueAt(baris, 1) != null ? jTableStudent.getValueAt(baris, 1).toString() : "";
        String nim   = jTableStudent.getValueAt(baris, 2) != null ? jTableStudent.getValueAt(baris, 2).toString() : "";
        String prodi = jTableStudent.getValueAt(baris, 3) != null ? jTableStudent.getValueAt(baris, 3).toString() : "";

        // paksa data masuk ke kotak input atas
        txtNIK.setText(nik);
        txtNama.setText(nama);
        txtNIM.setText(nim);
        cmbProdi.setSelectedItem(prodi);
        
        System.out.println("Berhasil klik tabel! Data NIK: " + nik); // untuk cek di panel output
        
        // mengunci kotak nik agar tidak bisa diubah-ubah saat edit
        txtNIK.setEditable(false);

    } catch (Exception e) {
        System.out.println("Gagal memindahkan data tabel: " + e.getMessage());
        }
    }//GEN-LAST:event_jTableStudentMouseClicked

